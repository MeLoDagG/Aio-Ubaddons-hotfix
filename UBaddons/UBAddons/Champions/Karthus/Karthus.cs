        #region Damage

        #region DamageRaw
        protected static float QDamage(Obj_AI_Base target)
        {
            var pred = Prediction.Position.PredictUnitPosition(target, 600);
            if (ObjectManager.Get<Obj_AI_Base>().Count(x => x.IsValidTarget(Q.Radius, false, pred.To3DWorld())) < 2)
            {
                return player.CalculateDamageOnUnit(target, DamageType.Magical, new[] { 0f, 80f, 120f, 160f, 200f, 240f }[Q.Level] + 0.6f * Player.Instance.TotalMagicalDamage);

            }
            else
            {
                return player.CalculateDamageOnUnit(target, DamageType.Magical, new[] { 0f, 40f, 60f, 80f, 100f, 120f }[Q.Level] + 0.3f * Player.Instance.TotalMagicalDamage);
            }
        }

        protected static float WDamage(Obj_AI_Base target)
        {
            return 0;
        }

        protected static float EDamage(Obj_AI_Base target)
        {
            return player.CalculateDamageOnUnit(target, DamageType.Magical, new[] { 0f, 30f, 50f, 70f, 90f, 110f }[E.Level] + 0.2f * Player.Instance.TotalMagicalDamage);
        }

        protected static float RDamage(Obj_AI_Base target)
        {
            return player.CalculateDamageOnUnit(target, DamageType.Magical, new[] { 0f, 250f, 400f, 550f }[R.Level] + 0.6f * Player.Instance.TotalMagicalDamage);
        }
        #endregion

        internal static float HandleDamageIndicator(Obj_AI_Base target, SpellSlot? slot = null)
        {
            if (target == null)
            {
                return 0;
            }
            switch (slot)
            {
                case SpellSlot.Q:
                    return QDamage(target);
                case SpellSlot.W:
                    return WDamage(target);
                case SpellSlot.E:
                    return EDamage(target);
                case SpellSlot.R:
                    return EDamage(target);
                default:
                    {
                        float damage = 0f;

                        if (Q.IsReady())
                        {
                            damage = damage + QDamage(target);
                        }
                        if (W.IsReady())
                        {
                            damage = damage + WDamage(target);
                        }
                        if (E.IsReady())
                        {
                            damage = damage + EDamage(target);
                        }
                        if (R.IsReady())
                        {
                            damage = damage + RDamage(target);
                        }
                        if (Orbwalker.CanAutoAttack)
                        {
                            damage = damage + player.GetAutoAttackDamage(target, true);
                        }
                        return damage;
                    }
            }
        }
